x=int(input("숫자1 입력:"))
y=int(input("숫자2 입력:"))
w=input("연산자 : ")
if w=='+':
    print(x,w,y,"=",x+y)
elif w=='-':
    print(x,w,y,"=",x-y)
elif w=='*':
    print(x,w,y,"=",x*y)
elif w=='/':
    print(x,w,y,"=",x/y)
elif w=='//':
    print(x,w,y,"=",x//y)
elif w=='%':
    print(x,w,y,"=",x%y)


